int
main(void)
{
	size_t i;
	uint8_t buffer[256];
	uint32_t crc;
	int rv;

	rv = 0;
	for (i = 0; i < sizeof(buffer); ++i)
		buffer[i] = (uint8_t)i;
	for (i = 0; i < sizeof(buffer); ++i) {
		/* crc = crc32_update_base(0xffffffff,
				buffer, i) ^ 0xffffffff; */
		crc = crc32(buffer, i);
		printf("#%3zu: 0x%08x\n", i, crc);
		if (crc != crc32_expect_table[i]) {
			printf("Failed at i == %zu\n", i);
			rv = 1;
		}
	}

	return rv;
}

